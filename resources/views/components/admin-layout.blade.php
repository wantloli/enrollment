    document.addEventListener('DOMContentLoaded', function() {
        var dropdown = document.getElementById('userDropdown');
        var dropdownButton = document.getElementById('userDropdownButton');
        var dropdownIcon = document.getElementById('dropdownIcon');

        // Check the saved state in localStorage
        if (localStorage.getItem('dropdownState') === 'open') {
            dropdown.classList.remove('hidden');
            dropdown.classList.add('opacity-100', 'translate-y-0');
            dropdownIcon.classList.add('rotate-180');
        }

        dropdownButton.addEventListener('click', function() {
            if (dropdown.classList.contains('hidden')) {
                dropdown.classList.remove('hidden');
                setTimeout(() => {
                    dropdown.classList.remove('opacity-0', '-translate-y-1');
                    dropdown.classList.add('opacity-100', 'translate-y-0');
                }, 50);
                dropdownIcon.classList.add('rotate-180');
                localStorage.setItem('dropdownState', 'open');
            } else {
                dropdown.classList.add('opacity-0', '-translate-y-1');
                dropdown.classList.remove('opacity-100', 'translate-y-0');
                setTimeout(() => {
                    dropdown.classList.add('hidden');
                }, 200);
                dropdownIcon.classList.remove('rotate-180');
                localStorage.setItem('dropdownState', 'closed');
            }
        });

